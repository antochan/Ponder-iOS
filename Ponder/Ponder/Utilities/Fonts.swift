import UIKit

extension UIFont {
    class func georgia(size: CGFloat) -> UIFont {
        return UIFont(name: "Georgia", size: size)!
    }
    
    class func georgiaBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Georgia-Bold", size: size)!
    }
}
